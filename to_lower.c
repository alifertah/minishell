#include "minishell.h"

void    to_lower(char *str)
{
    int i;

    if(!str)
        return;
    i = 0;
    while(str[i])
    {
        if(str[i] >= 'A' && str[i] <='Z')
            str[i] = str[i] + 32;
        i++;
    }
}